package com.cloudant.search3;

import io.grpc.health.v1.HealthCheckRequest;
import io.grpc.health.v1.HealthCheckResponse;
import io.grpc.health.v1.HealthCheckResponse.ServingStatus;
import io.grpc.health.v1.HealthGrpc.HealthImplBase;
import io.grpc.stub.StreamObserver;

public class Health extends HealthImplBase {

    @Override
    public void check(final HealthCheckRequest request, final StreamObserver<HealthCheckResponse> responseObserver) {
        final HealthCheckResponse response = HealthCheckResponse.newBuilder().setStatus(ServingStatus.SERVING).build();
        responseObserver.onNext(response);
        responseObserver.onCompleted();
    }

}
